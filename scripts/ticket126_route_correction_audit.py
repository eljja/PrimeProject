    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--execute-holdout",
        action="store_true",
        help="Execute the preregistered 32M holdout exactly once.",
    )
    args = parser.parse_args()
    twin = (
        execute_twin_holdout()
        if args.execute_holdout
        else _load_committed_twin_holdout()
    )
    audit = build_audit(twin)
    if int(audit["machine_audit"]["total_failure_count"]) != 0:
        raise ValueError("TICKET126 machine audit failed")
    write_outputs(audit)
    return 0


