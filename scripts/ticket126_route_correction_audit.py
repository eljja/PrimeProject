def write_outputs(audit: dict[str, Any]) -> None:
    attempts = build_attempts(audit)
    payload = {
        "schema": SCHEMA,
        "generated_at": GENERATED_AT,
        "status": "route_corrections_and_one_missing_premise_closed_all_conjectures_open",
        "claim_boundary": audit["proof_boundary"],
        "route_correction_audit": audit,
        "attempts": attempts,
    }
    write_json(
        ROOT / "data/open-problem/ticket126-route-correction-audit.json",
        payload,
    )
    paths = {
        "riemann": ROOT
        / "data/open-problem/riemann/rh-ticket-126-autocorrelation-density-no-go.json",
        "collatz": ROOT
        / "data/open-problem/collatz/co-ticket-126-eventually-low-path.json",
        "goldbach": ROOT
        / "data/open-problem/goldbach/gb-ticket-126-prime-power-bound.json",
        "twin-prime": ROOT
        / "data/open-problem/twin-prime/tp-ticket-126-32m-contraction-holdout.json",
    }
    for attempt in attempts:
        write_json(
            paths[attempt["problem_id"]],
            {"schema": SCHEMA, "generated_at": GENERATED_AT, **attempt},
        )


